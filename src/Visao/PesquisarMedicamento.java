    /**
     * Método responsável pela pesquisa de medicamentos pelo nome com filtro
     */
    private void pesquisarMedicamento() {
        String sql = "select idMedicamento as Id, nomemedicamento as Medicamento, laboratorio as Laboratório, apresentacao as Apresentação from tb_medicamento where nomemedicamento like ?";
        try {
            conexao = Bd_Conexao.conectar();
            pst = conexao.prepareStatement(sql);
            pst.setString(1, txtMedicamento.getText() + "%");
            rs = pst.executeQuery();
            tblMedicamentos.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        } finally {
            try {
                conexao.close();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, ex);
            }
        }
    }
    
