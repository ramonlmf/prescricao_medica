    private void btAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAlterarActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tableModel = (DefaultTableModel) tblMedicamentos.getModel();
        int row = tblMedicamentos.getSelectedRow();
        int id_medicamento_selecionado = Integer.parseInt(tableModel.getValueAt(row, 0).toString());   
        CadastroMedicamento form = new CadastroMedicamento();
        form.setFormQueAbriu(this); // 'this' é o formulário que está abrindo o geral 
        form.setarCamposDaPesquisaMedicamento(id_medicamento_selecionado);
        btAlterar.setEnabled(false);
        form.setVisible(true); 
        
    }//GEN-LAST:event_btAlterarActionPerformed

