    /**
     * Método responsável pela pesquisa de farmaceutico pelo nome com filtro
     * 
     * INSERT INTO tb_farmaceutico(idFarmaceutico, nome, farmacia, endereco, telefone, email, estado, cidade)
     */
    private void pesquisarFarmaceuticos() {
        String sql = "select idFarmaceutico as Id, nome as Nome, farmacia as Farmacia, endereco as Endereço, telefone as Telefone, email as Email, estado as Estado, cidade as Cidade from tb_farmaceutico where nome like ?";
        try {
            conexao = Bd_Conexao.conectar();
            pst = conexao.prepareStatement(sql);
            pst.setString(1, txtNome.getText() + "%");
            rs = pst.executeQuery();
            tblFarmaceuticos.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        } finally {
            try {
                conexao.close();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, ex);
            }
        }
    }
    
    /**
     * Método responsável para prencher a tabela com farmaceuticos na inicializacao
     */
    public void carregarTabelaFarmaceuticos() {
        String sql = "select idFarmaceutico as Id, nome as Nome, farmacia as Farmacia, endereco as Endereço, telefone as Telefone, email as Email, estado as Estado, cidade as Cidade from tb_farmaceutico";
        try {
            conexao = Bd_Conexao.conectar();
            pst = conexao.prepareStatement(sql);
            rs = pst.executeQuery();
            tblFarmaceuticos.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        } finally {
            try {
                conexao.close();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, ex);
            }
        }
    }

