        DefaultTableModel tableModel = (DefaultTableModel) tblFarmaceuticos.getModel();
        int row = tblFarmaceuticos.getSelectedRow();
        int id_farmaceutico_selecionado = Integer.parseInt(tableModel.getValueAt(row, 0).toString());   
        CadastroFarmaceutico form = new CadastroFarmaceutico();
